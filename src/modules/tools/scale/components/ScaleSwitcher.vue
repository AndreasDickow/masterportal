<script>
import {mapGetters, mapActions, mapMutations} from "vuex";
import Tool from "../../Tool.vue";
import getters from "../store/gettersScaleSwitcher";
import mutations from "../store/mutationsScaleSwitcher";
/**
 * Tool to switch the scale of the map. Listens to changes of the map's scale and sets the scale to this value.
 */
export default {
    name: "ScaleSwitcher",
    components: {
        Tool
    },
    computed: {
        ...mapGetters("Tools/ScaleSwitcher", Object.keys(getters)),
        ...mapGetters("Map", ["scale", "scales"])
    },
    watch: {
        /**
         * Watch to changes of scale triggered by map, e.g. by zoom. Set the selectbox to current scale.
         * @param {number} newValue the changed scale value
         * @returns {void}
         */
        scale (newValue) {
            if (newValue !== this.currentScale) {
                this.setCurrentScale(Math.round(this.scale / 1000) * 1000);
            }
        }
    },
    /**
     * Lifecycle hook: adds a "close"-Listener to close the tool.
     * @returns {void}
     */
    created () {
        this.$on("close", this.close);
    },
    /**
     * Put initialize here if mounting occurs after config parsing
     * @returns {void}
     */
    mounted () {
        this.initialize();
        if (this.isActive) {
            this.setActive(true);
        }
        this.activateByUrlParam();
    },
    methods: {
        ...mapActions("Tools/ScaleSwitcher", [
            "activateByUrlParam",
            "initialize"
        ]),
        ...mapActions("Map", ["setResolutionByIndex"]),
        ...mapMutations("Tools/ScaleSwitcher", Object.keys(mutations)),
        /**
         * Sets this current scale and map's scale.
         * @param {object} event of the select
         * @returns {void}
         */
        selectionChanged (event) {
            this.setCurrentScale(event.target.value);
            this.setResolutionByIndex(event.target.selectedIndex);
        },
        /**
         * Sets active to false.
         * @returns {void}
         */
        close () {
            this.setActive(false);

            // TODO replace trigger when ModelList is migrated
            // set the backbone model to active false in modellist for changing css class in menu (menu/desktop/tool/view.toggleIsActiveClass)
            const model = Radio.request("ModelList", "getModelByAttributes", {id: this.$store.state.Tools.ScaleSwitcher.id});

            if (model) {
                model.set("isActive", false);
            }
        }
    }
};
</script>

<template lang="html">
    <Tool
        :title="name"
        :icon="glyphicon"
        :active="active"
        :render-to-window="renderToWindow"
        :resizable-window="resizableWindow"
        :deactivateGFI="deactivateGFI"
    >
        <template v-slot:toolBody>
            <div
                v-if="active"
                id="scale-switcher"
            >
                <label
                    for="scale-switcher-select"
                    class="col-md-5 col-sm-5 control-label"
                >{{ $t("modules.tools.scaleSwitcher.label") }}</label>
                <div class="col-md-7 col-sm-7">
                    <select
                        id="scale-switcher-select"
                        class="font-arial form-control input-sm pull-left"
                        @change="selectionChanged($event)"
                    >
                        <option
                            v-for="(scale, i) in scales"
                            :key="i"
                            :value="scale"
                            :SELECTED="scale === currentScale"
                        >
                            1:{{ scale }}
                        </option>
                    </select>
                </div>
            </div>
        </template>
    </Tool>
</template>

